function verifyMatrixNums(target, matrix) {
  let counterRepeated = 0;
  let matrixNotRepeated = [];
  for (let i = 0; i < matrix.length; i += 1) {
    let notRepeated = [];
    for (let j = 0; j < matrix[i].length; j += 1) {
      if (matrix[i][j] === target) {
        counterRepeated += 1;
      } else {
        notRepeated.push(matrix[i][j]);
      }
    }

    matrixNotRepeated.push(notRepeated);
  }

  return { counter: counterRepeated, notRepeated: matrixNotRepeated };
}

