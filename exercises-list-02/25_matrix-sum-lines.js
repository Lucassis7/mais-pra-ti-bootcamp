function sumLines(matrix) {
  const sumLines = [];
  for (let i = 0; i < matrix.length; i += 1) {
    let sum = 0;
    for (let j = 0; j < matrix[i].length; j += 1) {
      sum += matrix[i][j];
    }

    sumLines.push(sum);
  }

  return sumLines;
}

